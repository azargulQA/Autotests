package org.sanbox.tests.tests;// Generated by Selenium IDE
import org.junit.jupiter.api.Assertions;
import org.junit.jupiter.api.BeforeEach;
import org.junit.jupiter.api.Test;
import org.sanbox.tests.model.GroupData;

import java.util.List;

public class GroupDeletionTest extends TestBase {

  @BeforeEach
  public void ensurePreconditions() {
    app.goTo().groupPage();
    if (app.group().list().size() == 0) {
      app.group().create(new GroupData().withName("test1"));
    }
  }

  @Test
  public void groupDeletion() {
    List<GroupData> before = app.group().list(); //начало проверки на количество
    int index = before.size() - 1;
    app.group().delete(index);
    List<GroupData> after = app.group().list(); //конец проверки на количество
    Assertions.assertEquals(after.size(), before.size() - 1); //проверка на количество

    before.remove(before.size() - 1);
    for (int i = 0; i < after.size(); i++) {
      Assertions.assertEquals(before.get(i), after.get(i));
    }
  }


}
